# === Private-mode installer for vifi-site ===
# Adds /results + /roadmap pages on vifi.health, removes all github.com
# links from the site, reworks the 'open' section. After running this,
# you can safely make Zpopowitz/vifi-ml private without breaking the site.

$ErrorActionPreference = 'Stop'
$baseDir = if ($PSScriptRoot) { $PSScriptRoot } else { (Get-Location).Path }
if (-not (Test-Path (Join-Path $baseDir 'package.json'))) {
    Write-Error "Run this from inside the vifi-site repo (next to package.json)."
    exit 1
}

Write-Host "Applying private-mode rewrite to: $baseDir" -ForegroundColor Cyan

# Make sure new directories exist
foreach ($d in @('src/layouts')) {
    $full = Join-Path $baseDir $d
    if (-not (Test-Path $full)) { New-Item -ItemType Directory -Path $full | Out-Null }
}

function Write-FileFromBase64($relPath, $b64) {
    $full = Join-Path $baseDir $relPath
    $exists = Test-Path $full
    $bytes = [Convert]::FromBase64String($b64)
    [System.IO.File]::WriteAllBytes($full, $bytes)
    $tag = if ($exists) { 'updated' } else { 'created' }
    Write-Host "  $tag $relPath ($($bytes.Length) bytes)"
}

